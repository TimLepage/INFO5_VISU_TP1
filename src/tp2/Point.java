package tp2;

public class Point {
	private double x;
	private double y;

	public Point(double x1, double y1) {
		this.x = x1;
		this.y = y1;

	}

	public double getX() {
		return this.x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return this.y;
	}

	public void setY(double y) {
		this.y = y;
	}
}
